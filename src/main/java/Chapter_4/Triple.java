package Chapter_4;

public class Triple {

    int i;
    int j;
    double distance;

    public Triple(int i, int j, double distance) {
        this.i = i;
        this.j = j;
        this.distance = distance;
    }

    public int getI() {
        return i;
    }

    public int getJ() {
        return j;
    }

    public double getDistance() {
        return distance;
    }
}
